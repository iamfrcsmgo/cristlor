    <h1>L'éditeur de texte :</h1>
    <h2>Qu&rsquo;est-ce qu&rsquo;un &eacute;diteur</h2>

<p>Il existe des milliers d&rsquo;&eacute;diteurs disponibles sur le web, fonctionnant avec des environnements diff&eacute;rents (Windows, Mac, Linux, etc.).<br>
  Le plus connu de tous est certainement Sublime Text, le fameux &eacute;diteur fonctionnant avec Windows, Mac, Linux.</p>

<p>Un &eacute;diteur va nous permettre de pouvoir &eacute;crire des pages de code et de les enregistrer au bon format (c&rsquo;est-&agrave;-dire avec la bonne extension).<br>
   Les &eacute;diteurs proposent en g&eacute;n&eacute;ral diff&eacute;rentes options pour vous aider &agrave; coder, comme par exemple des indications lorsque vous fa&icirc;tes une faute de syntaxe dans votre code.</p>

<h2>Editeur contre site</h2>

<p>Certains d&rsquo;entre vous connaissent peut &ecirc;tre le site&nbsp;<strong>jsbin.com</strong>&nbsp;?</p>

<p>En effet, vous n&rsquo;&ecirc;tes pas strictement oblig&eacute;s de passer par un &eacute;diteur pour apprendre &agrave; coder en HTML et en CSS, car de nombreux sites comme JS Bin vous proposent de rentrer directement votre code en HTML et en CSS et affichent le r&eacute;sultat en direct.</p>

<p>Ces sites sont tr&egrave;s pratiques lorsque vous voulez tester rapidement un bout de code ou pour des d&eacute;monstrations de cours.</p>

<p>Cependant, retenez bien qu&rsquo;ils sont aussi limit&eacute;s : vous ne pourrez pas faire certaines choses via ces sites et surtout, si vous devez un jour cr&eacute;er un site avec plusieurs pages reli&eacute;es entre elles, vous devrez obligatoirement utiliser un &eacute;diteur.</p>
