  <h3>Qu'est-ce qu'un cookie ?</h3>

  <p>Les cookies sont de petits fichiers que les sites Web mettent sur le disque dur de votre ordinateur lors de votre première visite.<br>
Représentez-vous un cookie comme une carte d'identification qui est uniquement la vôtre. Son travail est d'indiquer au site lorsque vous y retournez. Bien qu'il soit possible d'en faire une mauvaise utilisation dans des cas où ils renferment des données personnelles, les cookies ne sont pas par eux-mêmes malveillants.<br>
La plupart des sites Web, y compris Microsoft, utilisent des cookies. Les cookies nous indiquent la fréquentation des pages que vous visitez, ce qui nous aide à connaître les informations qui vous intéressent. Ainsi, nous pouvons vous donner plus de contenu qui vous intéresse et moins de contenu qui ne vous intéresse pas.<br>
Les cookies peuvent vous aider à être plus efficace. Avez-vous déjà mis un article dans un panier virtuel sur une boutique en ligne et y êtes-vous retourné quelques jours plus tard et avez-vous vu que votre article s'y trouve toujours ? C'est un exemple du travail des cookies.<br>
Les cookies vous permettent de stocker des préférences et les noms des utilisateurs, enregistrer des produits et des services et personnaliser des pages.<br>
Mais si vous n'avez jamais enregistré ou laissé d'informations personnelles sur un site, alors le serveur sait simplement que quelqu'un avec votre cookie est revenu sur le site Web. Il ne sait rien d'autre.
</p>

