        <h1>Description :</h1>
        <p>HTML permet d&#39;inclure des images et d&#39;autres contenus dans les pages web.<br>
   Gr&acirc;ce &agrave; HTML, chacun peut cr&eacute;er des sites web aussi bien statiques que dynamiques<span style="font-size: 1.4em;color: red;">&#42;</span>.<br>
   HTML est le langage qui permet de d&eacute;crire la structure et le contenu d&#39;un document web.<br>
   Ce contenu est balis&eacute; par des &eacute;l&eacute;ments HTML comme :<br>
   &nbsp; <code>&lt;head&gt; </code>,&nbsp; <code>&lt;title&gt; </code>,&nbsp; <code>&lt;body&gt; </code>,&nbsp; <code>&lt;header&gt; </code>,&nbsp;&nbsp; <code>&lt;footer&gt; </code>,&nbsp; <code>&lt;article&gt; </code>,&nbsp; <code>&lt;section&gt; </code>,&nbsp; <code>&lt;p&gt; </code>,&nbsp; <code>&lt;div&gt; </code>,&nbsp; <code>&lt;span&gt; </code>,&nbsp; <code>&lt;img&gt; </code>&nbsp;et bien d&#39;autres encore.<br>
   Ces &eacute;l&eacute;ments forment les blocs utilis&eacute;s pour construire un site web.</p>

<p>En HTML, les balises sont insensibles &agrave; la casse et peuvent aussi bien &ecirc;tre &eacute;crites en minuscules, en majuscules voire avec un m&eacute;lange des deux.<br>
   Autrement dit, la balise&nbsp; <code>&lt;title&gt; </code>pourrait tr&egrave;s bien &ecirc;tre &eacute;crite comme&nbsp; <code>&lt;Title&gt; </code>,&nbsp; <code>&lt;TiTle&gt; </code>&nbsp;ou d&#39;une autre fa&ccedil;on.</p>
   <p><span style="font-size: 1.4em;color: red;">&#42;</span>
   - Site statique :<br> Une page web statique est une page web dont le contenu ne varie pas en fonction des caractéristiques de la demande, c'est-à-dire qu'à un moment donné tous les internautes qui demandent la page reçoivent le même contenu.<br><br>
   <span style="font-size: 1.4em;color: red;">&#42;</span>
   - Site dynamique :<br> Une page web dynamique est générée à la demande et son contenu varie en fonction des caractéristiques de la demande (heure, adresse IP de l'ordinateur du demandeur, formulaire rempli par le demandeur, etc.) qui ne sont connues qu'au moment de sa consultation.</p>
